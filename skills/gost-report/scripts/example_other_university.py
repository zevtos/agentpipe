"""Пример: курсовая работа в произвольном вузе через GOST_PROFILE + UniversityProfile.

Показывает, как использовать skill для не-ИТМО университета. Профиль строится
из GOST_PROFILE (чистый ГОСТ 7.32) с переопределением только нужных полей —
обычно достаточно указать название вуза, факультет и город.

Все ФИО — плейсхолдеры (Фамилия И.О.), пользователь подставляет свои.
"""

from gost_report import Report, TitleConfig, UniversityProfile


# Сборка профиля своего вуза. Скопируйте этот блок и поправьте под себя.
MY_UNIVERSITY = UniversityProfile(
    university_full=(
        "федеральное государственное бюджетное "
        "образовательное учреждение высшего образования"
    ),
    university_short="«Название вашего университета»",
    faculty="Название вашего факультета",
    city="Город",
    # Всё остальное (поля, кегли, «СОДЕРЖАНИЕ») — из GOST_PROFILE по умолчанию.
)


def main():
    r = Report(
        TitleConfig(
            work_type="Курсовая работа",
            topic="Тема вашей курсовой работы без кавычек",
            student_name="Фамилия И.О.",
            student_group="ГРУППА",
            teacher_name="Фамилия И.О.",
            teacher_degree="к.т.н.",
            teacher_position="доцент",
            teacher_label="Руководитель",  # для курсовых/ВКР
            year="2026",
        ),
        profile=MY_UNIVERSITY,
    )

    r.toc()

    r.h1("Введение")
    r.text("Актуальность темы исследования обусловлена...")
    r.text("Цель работы — ...")
    r.text("Для достижения поставленной цели решаются следующие задачи: ...")

    r.h1("Глава 1. Теоретические основы")
    r.h2("1.1. Постановка задачи")
    r.text("...")
    r.h2("1.2. Обзор существующих подходов")
    r.text("...")

    r.h1("Глава 2. Практическая часть")
    r.text("...")

    r.h1("Заключение")
    r.numbered([
        "Изучены теоретические основы предметной области.",
        "Реализована практическая часть согласно постановке задачи.",
        "Получены результаты, подтверждающие выводы исследования.",
    ])

    r.h1("Список литературы")
    r.text("1. Источник 1 — оформить по ГОСТ 7.1-2003.")
    r.text("2. Источник 2.")

    r.save("coursework.docx")
    print("Saved: coursework.docx — обновите оглавление в Word.")


if __name__ == "__main__":
    main()

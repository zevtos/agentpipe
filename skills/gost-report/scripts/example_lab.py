"""Пример: лабораторная работа по командной строке Unix (профиль ИТМО).

Этот файл — образец того, как из вызовов API получается готовый отчёт.
Скопируйте, замените заголовки и текст под свою работу, запустите.

Все ФИО — плейсхолдеры (Фамилия И.О.), пользователь подставляет свои.
"""

from gost_report import Report, TitleConfig
# ITMO_PROFILE — профиль по умолчанию, импортировать необязательно


def main():
    r = Report(TitleConfig(
        work_type="Лабораторная работа",
        work_number="№1",
        topic="Основы работы в командной строке Unix",
        student_name="Фамилия И.О.",
        student_group="P3XXX",
        teacher_name="Фамилия И.О.",
        teacher_degree="к.т.н.",
        teacher_position="доцент",
        year="2026",
    ))

    # Оглавление (само заполнится при открытии в Word через "Обновить поле")
    r.toc()

    # --- Введение ---
    r.h1("Введение")
    r.text(
        "Цель работы — освоить базовые команды командной строки Unix "
        "для навигации по файловой системе, работы с файлами и каталогами, "
        "а также управления процессами."
    )
    r.text(
        "В ходе выполнения работы используется командная оболочка bash на "
        "учебном Linux-сервере; все команды выполняются в интерактивном режиме."
    )

    # --- Выполнение работы ---
    r.h1("Выполнение работы")

    r.h2("Задание 1. Просмотр содержимого каталога")
    r.task("Задание 1. Вывести подробный список файлов текущего каталога.")
    r.code("ls -la")
    r.text(
        "Команда выводит все файлы и каталоги (включая скрытые), показывая "
        "права доступа, владельца, размер и дату последнего изменения."
    )

    r.h2("Задание 2. Создание иерархии каталогов")
    r.task("Задание 2. Создать вложенную структуру каталогов src/main/java одной командой.")
    r.code("mkdir -p src/main/java")
    r.text("Флаг -p создаёт промежуточные каталоги и не выдаёт ошибку, если они уже существуют.")

    # --- Заключение ---
    r.h1("Заключение")
    r.numbered([
        "Освоены команды навигации по файловой системе (ls, cd, pwd).",
        "Освоены команды работы с файлами и каталогами (cp, mv, rm, mkdir).",
        "Получены навыки построения сложных команд через конвейер.",
    ])

    r.save("report.docx")
    print("Saved: report.docx — откройте в Word и обновите оглавление (ПКМ → «Обновить поле»).")


if __name__ == "__main__":
    main()
